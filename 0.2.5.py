# range(fin) возвращает последовательность чисел от 0 до f in − 1 с шагом 1;
# range(start, fin) возвращает последовательность от start до f in − 1 с шагом 1;
# range(start, fin, step) возвращает последовательность от start до f in − 1 с шагом step

for i in range(3):
    print(i)

for i in range(0, 10):
    print(i)

for i in range(0, 8, 2):
    print(i)

for i in range(10, 0, -1):
    print(i)

